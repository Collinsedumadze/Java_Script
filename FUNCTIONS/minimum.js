// Like the standard function Math.min that returns its smallest argument. Define the function min that takes two arguments and returns their minimum.
let min = 0;

function getMin(x , y){
    if(x > y){
       return min += y; 
    }else{
       return min += x;
    }

}

console.log(getMin(0, 4));

